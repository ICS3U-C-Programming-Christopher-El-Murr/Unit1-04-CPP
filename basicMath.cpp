#include <iostream>
#include<cmath>

int main() {
    std::cout << "8+7=" << (8 + 7) << std::endl;
    std::cout << "8-7=" << (8 - 7) << std::endl;
    std::cout << "8/2=" << (8/2) << std::endl;
    std::cout << "8*7=" << (8*7) << std::endl;
    std::cout << "8+7/7=" << (8+7/7) << std::endl;
    std::cout << "8+pow(7,4)=" << (8+pow(7,4)) << std::endl;
}